export function hideElement(element) {
  if (element) {
    element.style.display = "none";
  }
}

export function showFlexElement(element) {
  if (element) {
    element.style.display = "flex";
  }
}

export function showBlockElement(element) {
  if (element) {
    element.style.display = "block";
  }
}
